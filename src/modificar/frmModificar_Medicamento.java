    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        String n= Medicamento.getText();
        String m= String.valueOf(mes.getSelectedIndex()+1);
        String a= ano.getSelectedItem().toString();
        int id=0;
        id=consulta.id;
        int c;
        try {
            c= Integer.parseInt(Cantidad.getText());
        } catch (Exception e) {
            c=-1;
        }

        if(n.equals("")||c==-1)
        {
            JOptionPane.showMessageDialog(null, "Error dato incorrecto");
            Medicamento.setText("");
            Cantidad.setText("");
        }
        else{
            sql.EjecutarConsultas("call update_M("+id+",'"+n+"',"+c+",'"+a+"-"+m+"-1"+"')");
            //sql.EjecutarConsultas("call insertarmedicamento('"+n+"',"+c+",'"+a+"-"+m+"-1"+"')");
            consulta.llenartabla();
            JOptionPane.showMessageDialog(null, "Modificado con Exito");
            Medicamento.setText("");
            Cantidad.setText("");
            this.dispose();
        }
    }//GEN-LAST:event_guardarActionPerformed

